/**
 * –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –Ω–æ–º–µ—Ä –Ω–µ–¥–µ–ª–∏ –º–µ–Ω—é (1 –∏–ª–∏ 2) –Ω–∞ –æ—Å–Ω–æ–≤–µ —Ç–µ–∫—É—â–µ–π –¥–∞—Ç—ã
 * –ë–∞–∑–æ–≤–∞—è –¥–∞—Ç–∞ - 2 –¥–µ–∫–∞–±—Ä—è 2024 (–ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ –Ω–µ–¥–µ–ª–∏ 1)
 */
export function getCurrentWeekNumber(): number {
  const baseDate = new Date(2024, 11, 2); // 2 –¥–µ–∫–∞–±—Ä—è 2024
  const currentDate = new Date();

  // –°–±—Ä–∞—Å—ã–≤–∞–µ–º –≤—Ä–µ–º—è –¥–ª—è —Ç–æ—á–Ω–æ–≥–æ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è –¥–∞—Ç
  baseDate.setHours(0, 0, 0, 0);
  currentDate.setHours(0, 0, 0, 0);

  const diffTime = currentDate.getTime() - baseDate.getTime();
  const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));
  const diffWeeks = Math.floor(diffDays / 7);

  // –ß–µ—Ç–Ω–∞—è –Ω–µ–¥–µ–ª—è - –Ω–µ–¥–µ–ª—è 1, –Ω–µ—á–µ—Ç–Ω–∞—è - –Ω–µ–¥–µ–ª—è 2
  return (diffWeeks % 2) + 1;
}

/**
 * –ü–æ–ª—É—á–∞–µ—Ç —Ç–µ–∫—É—â–∏–π –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏ (0-4 –¥–ª—è –ü–Ω-–ü—Ç, null –¥–ª—è –≤—ã—Ö–æ–¥–Ω—ã—Ö)
 */
export function getCurrentDayIndex(): number | null {
  const today = new Date();
  const dayOfWeek = today.getDay();

  // –í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ = 0, –ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ = 1, ... –ü—è—Ç–Ω–∏—Ü–∞ = 5, –°—É–±–±–æ—Ç–∞ = 6
  // –ü–µ—Ä–µ–≤–æ–¥–∏–º –≤ –Ω–∞—à —Ñ–æ—Ä–º–∞—Ç: –ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ = 0, ... –ü—è—Ç–Ω–∏—Ü–∞ = 4
  if (dayOfWeek === 0 || dayOfWeek === 6) {
    return null; // –í—ã—Ö–æ–¥–Ω–æ–π
  }

  return dayOfWeek - 1;
}
